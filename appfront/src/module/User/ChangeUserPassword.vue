<script>
import { ref, reactive } from 'vue';
import { ElMessage } from 'element-plus';
// import {GetUid} from "@/module/User/UserFunctionLib";

export default {
    components: {},
    props: {},
    name: 'ChangeUserPassword',
    data() {
        return {
            uid: '',
            formData: {
                oldpassword: "",
                newpassword: "",
                curpassword: "",
            },
            rules: {
                oldpassword: [{
                    required: true,
                    message: '字段值不可为空',
                }],
                newpassword: [{
                    required: true,
                    message: '字段值不可为空',
                }, {
                    pattern: /^[A-Za-z0-9]+$/,
                    trigger: ['blur', 'change'],
                    message: ''
                }],
                curpassword: [{
                    required: true,
                    message: '字段值不可为空',
                }, {
                    pattern: /^[A-Za-z0-9]+$/,
                    trigger: ['blur', 'change'],
                    message: ''
                }],
            },
        }
    },
    computed: {},
    watch: {},
    created() {
        this.$data.uid = this.$route.query.uid;
    },
    mounted() {
    },
    methods: {
        submitForm() {
            this.$refs['vForm'].validate(valid => {
                if (!valid) {
                    ElMessage.error("请输入完整数据");
                    return;
                }
                // alert((this.$data.formData.curpassword) + ' ' + (this.$data.formData.newpassword));
                if (this.$data.formData.newpassword !== this.$data.formData.curpassword) {
                    ElMessage.error("两次输入的密码不一致！");
                    return;
                }
                let data = new FormData();
                data.append('oldpassword', this.$data.formData.oldpassword);
                data.append('newpassword', this.$data.formData.newpassword);
                this.axios.post('/AppUser/api/ChangeUserPassword/' + this.$data.uid, data).then((res) => {
                    if (res.data.code === 'ok') {
                        //
                        // alert("修改成功！");
                        this.$message({
                            message: '修改成功！',
                            type: 'success',
                            center: true,
                        });
                        window.location.href = '/login/';
                    } else {
                        ElMessage.error(res.data.message);
                    }
                })
                // alert( JSON.stringify(formData) );
            }).catch(error => {
                // Form Validation failed
                ElMessage.error(error);
                //TODO: 提交表单
            })
        },
        resetForm() {
            this.$refs['vForm'].resetFields()
        }
    }
}


            <el-form :model="formData" ref="vForm" :rules="rules" label-position="left" label-width="80px"
                size="default" @submit.prevent>
                <el-form-item label="原密码" prop="oldpassword" class="required">
                    <el-input v-model="formData.oldpassword" type="password" :show-password="true" clearable></el-input>
                </el-form-item>
                <el-form-item label="新密码" prop="newpassword" class="required">
                    <el-input v-model="formData.newpassword" type="password" :show-password="true" clearable></el-input>
                </el-form-item>
                <el-form-item label="确认密码" prop="curpassword" class="required">
                    <el-input v-model="formData.curpassword" type="password" :show-password="true" clearable></el-input>
                </el-form-item>
